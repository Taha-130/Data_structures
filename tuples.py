# Les tuples sont des collections ordonnées, mais immuables.
# Une fois qu'un tuple est créé, vous ne pouvez pas changer, ajouter ou supprimer ses éléments.

mon_tuple = (1, 2, 3, 4, 5)








# Accéder à un élément par son index
# Les tuples sont similaires aux listes en ce qui concerne l'accès aux éléments via l'index
print(mon_tuple[3])  # Affiche 4 (élément à l'index 3)

# Les tuples sont immuables, donc vous ne pouvez pas modifier leurs éléments une fois créés.
# La ligne suivante génère une erreur si on essaie de modifier un élément d'un tuple
# mon_tuple[0] = 10  # Provoque une erreur : TypeError









# Afficher le tuple
print(mon_tuple)  # Affiche (1, 2, 3, 4, 5)
