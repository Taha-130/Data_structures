# Les listes sont des collections ordonnées et modifiables.
# Cela signifie que les éléments de la liste ont un ordre défini
# et qu'on peut ajouter, modifier ou supprimer des éléments.

ma_liste = [1, 2, 3, 4, 5]


















# Ajouter un élément à la fin de la liste avec append()
# La méthode append() ajoute l'élément passé en paramètre à la fin de la liste
ma_liste.append(6)  
print(ma_liste) # # La liste devient [1, 2, 3, 4, 5, 6]











